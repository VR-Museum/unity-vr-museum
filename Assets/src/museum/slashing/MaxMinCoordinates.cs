/// <summary>
/// Структура, содержащая текущие максимальные и минимальные значения xyz координат.
/// </summary>
public struct MaxMinCoordinates
{
    /// <summary>
    /// Максимальная x координата.
    /// </summary>
    public float MaxX { get; set; }
    /// <summary>
    /// Максимальная y координата.
    /// </summary>
    public float MaxY { get; set; }
    /// <summary>
    /// Максимальная z координата.
    /// </summary>
    public float MaxZ { get; set; }
    /// <summary>
    /// Минимальная x координата.
    /// </summary>
    public float MinX { get; set; }
    /// <summary>
    /// Минимальная y координата.
    /// </summary>
    public float MinY { get; set; }
    /// <summary>
    /// Минимальная z координата.
    /// </summary>
    public float MinZ { get; set; }
}
